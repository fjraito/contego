export const STATIC_BLOG_SLUGS = new Set(STATIC_BLOG_POSTS.map((post) => post.slug))

export function getStaticBlogPost(slug) {
  return STATIC_BLOG_POSTS.find((post) => post.slug === slug) || null
}
